/**
 * Generates lib/content/network-map.ts — a dot-matrix rendering of
 * North America (US + MX + southern Canada) for the interactive NetworkMap.
 *
 * Usage: node scripts/generate-network-map.mjs
 * Sources: johan/world.geo.json country outlines (public domain).
 */

const SOURCES = {
  us: "https://raw.githubusercontent.com/johan/world.geo.json/master/countries/USA.geo.json",
  mx: "https://raw.githubusercontent.com/johan/world.geo.json/master/countries/MEX.geo.json",
  ca: "https://raw.githubusercontent.com/johan/world.geo.json/master/countries/CAN.geo.json",
};

// Projection window (equirectangular, cos-corrected at mid-latitude)
const LON_MIN = -130;
const LON_MAX = -65;
const LAT_MIN = 14;
const LAT_MAX = 55;
const VIEW_W = 100;
const MID_LAT = 33;
const KX = VIEW_W / (LON_MAX - LON_MIN);
const KY = KX / Math.cos((MID_LAT * Math.PI) / 180);
const VIEW_H = +( (LAT_MAX - LAT_MIN) * KY ).toFixed(2);

const STEP = 0.65; // degrees between dots

function project(lon, lat) {
  return [
    +(((lon - LON_MIN) * KX)).toFixed(2),
    +(((LAT_MAX - lat) * KY)).toFixed(2),
  ];
}

// Ray-casting point-in-ring
function inRing(lon, lat, ring) {
  let inside = false;
  for (let i = 0, j = ring.length - 1; i < ring.length; j = i++) {
    const [xi, yi] = ring[i];
    const [xj, yj] = ring[j];
    if (yi > lat !== yj > lat && lon < ((xj - xi) * (lat - yi)) / (yj - yi) + xi) {
      inside = !inside;
    }
  }
  return inside;
}

function inFeature(lon, lat, geometry) {
  const polys =
    geometry.type === "Polygon" ? [geometry.coordinates] : geometry.coordinates;
  for (const poly of polys) {
    // outer ring inside, and not inside any hole
    if (inRing(lon, lat, poly[0])) {
      let inHole = false;
      for (let r = 1; r < poly.length; r++) {
        if (inRing(lon, lat, poly[r])) {
          inHole = true;
          break;
        }
      }
      if (!inHole) return true;
    }
  }
  return false;
}

async function fetchGeometry(url) {
  const res = await fetch(url);
  if (!res.ok) throw new Error(`fetch failed ${res.status}: ${url}`);
  const gj = await res.json();
  return gj.features[0].geometry;
}

function dotsFor(geometry, { latMax = LAT_MAX } = {}) {
  const dots = [];
  for (let lat = LAT_MIN + STEP / 2; lat <= latMax; lat += STEP) {
    for (let lon = LON_MIN + STEP / 2; lon <= LON_MAX; lon += STEP) {
      if (inFeature(lon, lat, geometry)) dots.push(project(lon, lat));
    }
  }
  return dots;
}

const [us, mx, ca] = await Promise.all([
  fetchGeometry(SOURCES.us),
  fetchGeometry(SOURCES.mx),
  fetchGeometry(SOURCES.ca),
]);

const usDots = dotsFor(us);
const mxDots = dotsFor(mx);
const caDots = dotsFor(ca); // window clips Canada to its southern strip

const banner = `/**
 * AUTO-GENERATED by scripts/generate-network-map.mjs — do not edit by hand.
 * Dot-matrix North America for the interactive network map.
 * Projection: equirectangular, lon [${LON_MIN}, ${LON_MAX}], lat [${LAT_MIN}, ${LAT_MAX}].
 */
`;

const body = `${banner}
export const MAP_VIEW = { w: ${VIEW_W}, h: ${VIEW_H} } as const;

export const MAP_PROJECTION = {
  lonMin: ${LON_MIN},
  lonMax: ${LON_MAX},
  latMin: ${LAT_MIN},
  latMax: ${LAT_MAX},
  kx: ${+KX.toFixed(6)},
  ky: ${+KY.toFixed(6)},
} as const;

/** [x, y] in MAP_VIEW units */
export type MapDot = readonly [number, number];

export const MAP_DOTS_US: readonly MapDot[] = ${JSON.stringify(usDots)};

export const MAP_DOTS_MX: readonly MapDot[] = ${JSON.stringify(mxDots)};

export const MAP_DOTS_CA: readonly MapDot[] = ${JSON.stringify(caDots)};
`;

await import("node:fs/promises").then((fs) =>
  fs.writeFile(new URL("../lib/content/network-map.ts", import.meta.url), body)
);

console.log(
  `done: US ${usDots.length} dots, MX ${mxDots.length} dots, CA ${caDots.length} dots, viewBox 0 0 ${VIEW_W} ${VIEW_H}`
);
